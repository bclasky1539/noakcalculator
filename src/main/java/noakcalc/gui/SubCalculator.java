    private JPopupMenu createConstDropDownMenu() {
        Set<String> keyset = constantBundle. keySet();
        Iterator<String> iterator = keyset.iterator();
        while (iterator.hasNext()) {
            String key = iterator.next();
            JMenuItem menuItem = new JMenuItem();
            setObjectFromProp(menuItem, (String) constantBundle.getObject(key),
                MiscConstants.PIPE_DELIMITER);
            popupMenu.add(menuItem);
            menuItem.addActionListener((java.awt.event.ActionEvent evt) -> {
                jbtnKeyActionPerformed(evt);
            });
        }

        return popupMenu;
    }

    /**
     * Takes the mouse click or key and processes it
     *
     * @param actionCmdString
     */
    public void setCalcInfo(String actionCmdString) {
        LOGGER.debug("actionCmdString #" + actionCmdString + "#");
        LOGGER.debug("keyPressed #" + keyBrdInputHandler.isKeyPressed() + "#");

        calc.inputKey(actionCmdString);
        switch (actionCmdString) {
            case CalcConstants.MSX:
                jlblMem.setEnabled(true);
                break;
            case CalcConstants.MCX:
                jlblMem.setEnabled(false);
                break;
        // Should not happen
            default:
                break;
        }

        if (calc.isTxtareaDisplayText()) {
           LOGGER.debug("OutputDigits would happen");
           jtxtAreaOutput.append(calc.getSecScreenText() + "\n" + calc.getPrmDisplayText() + "\n\n");
        }

        if (!keyBrdInputHandler.isKeyPressed() || (keyBrdInputHandler.isKeyPressed() && " C ".equals(actionCmdString))) {
            LOGGER.debug("if block\n");

            jtxtPrimary.setText(calc.getPrmDisplayText());

            if(!actionCmdString.equals(CalcConstants.BSP)) {
                jtxtSecondary.setText(calc.getSecScreenText());
            }
        }

        jtxtPrimary.requestFocus();
    }

