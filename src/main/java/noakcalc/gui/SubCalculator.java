    /**
     * Sets the number system to be used. Also updates the click-ability of
     * the buttons based on the number system. Thus, if the number system is
     * BIN, buttons 3-9 are disabled.
     */
    private void setNumMode(String mode) {
        switch (mode) {
            case CalcConstants.HEX:
                setDecEnabled(true);
                setOctEnabled(true);
                setHexEnabled(true);
                break;
            case CalcConstants.DEC:
                setDecEnabled(true);
                setOctEnabled(true);
                setHexEnabled(false);
                break;
            case CalcConstants.OCT:
                setDecEnabled(true);
                setOctEnabled(false);
                setHexEnabled(false);
                break;
            case CalcConstants.BIN:
                setDecEnabled(false);
                setOctEnabled(false);
                setHexEnabled(false);
                break;
        }
        jbtnDecimal.setEnabled(mode.equals(CalcConstants.DEC));
        numMode = mode;
        calc.inputKey(mode);
    }

