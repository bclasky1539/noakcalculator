    private void setObjectFromProp(JButton jButtonName, String property, String delimiter) {
        propSplitStrings = UtilsMisc.stringSplit(property, delimiter);
        switch (propSplitStrings.length) {
            case 2:
                jButtonName.setActionCommand(propSplitStrings[0]);
                jButtonName.setText(propSplitStrings[0]);
                jButtonName.setToolTipText(propSplitStrings[1]);
                break;
            case 3:
                jButtonName.setActionCommand(propSplitStrings[2]);
                jButtonName.setText(propSplitStrings[0]);
                jButtonName.setToolTipText(propSplitStrings[1]);
                break;
            default:
                LOGGER.debug("Incorrect number of parameters: " + propSplitStrings.length);
                break;
        }
        jButtonName.setFont(new Font("Arial", Font.PLAIN, 13));
    }

